package controlflow;

public class FruitOptions {
public static void main(String[] args) {
String fruit="apple";

switch(fruit) {
case "mango":{
	System.out.println("have your mango");
	break;
	}	

case "banana":{
	System.out.println("have your banana");
	break;
	}	

case "apple":{
	System.out.println("have your apple");
	break;
	
}
default:{
	System.out.println("sorry we don't have "+fruit);
}
}
	
	
	
	}
	
	
	
	
}

