package controlflow;
/**
 * 10th percentage
 * 12th percentage
 * B.E percentage
 * backlogs
 * 
 *
 */
public class Student {
	double tenthPercentage;
	double twelthPercentage;
	double bePercentage;
	int backlogs;
	
	public Student(double tenthPercentage, double twelthPercentage, double bePercentage, int backlogs) {
		super();
		this.tenthPercentage = tenthPercentage;
		this.twelthPercentage = twelthPercentage;
		this.bePercentage = bePercentage;
		this.backlogs = backlogs;
	}
	public double getTenthPercentage() {
		return tenthPercentage;
	}
	public void setTenthPercentage(double tenthPercentage) {
		this.tenthPercentage = tenthPercentage;
	}
	public double getTwelthPercentage() {
		return twelthPercentage;
	}
	public void setTwelthPercentage(double twelthPercentage) {
		this.twelthPercentage = twelthPercentage;
	}
	public double getBePercentage() {
		return bePercentage;
	}
	public void setBePercentage(double bePercentage) {
		this.bePercentage = bePercentage;
	}
	public int getBacklogs() {
		return backlogs;
	}
	public void setBacklogs(int backlogs) {
		this.backlogs = backlogs;
	}
	
	
	

}